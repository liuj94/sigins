package com.example.zjsignin.bean;

public class CustomData {
    public String code;
    public String msg;
    public CustomResult data;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public CustomResult getData() {
        return data;
    }

    public void setData(CustomResult data) {
        this.data = data;
    }
}
